package ecossitema;

public class SerVivo {

	public String nome;
	public int QTD_Animal;
	public int idade;
	public char sexo;
	public boolean predador;
	
	public SerVivo (String nome, int QTD_Animal, int idade, char sexo, boolean predador) {
		this.nome = nome;
		this.QTD_Animal = QTD_Animal;
		this.idade = idade;
		this.sexo = sexo;
		this.predador = predador;
	}
}
